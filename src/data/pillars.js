export const conceptKey = (pillarId, si, ci) => `${pillarId}-${si}-${ci}`;
export const criterionKey = (pillarId, i) => `${pillarId}-crit-${i}`;

export function pillarCounts(p) {
  const concepts = p.subtopics.reduce((a, s) => a + s.concepts.length, 0);
  return { concepts, criteria: p.mastery.length, total: concepts + p.mastery.length };
}
